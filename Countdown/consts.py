"""The constants for the Countdown module"""

# time values
# the base unit is 1 second

second = 1
minute = second * 60
hour = minute * 60
day = hour * 24
week = day * 7
month = day * 30  # assuming an average month is 30 days. There should really be no need for an exact count.
year = day * 365  # same as month
